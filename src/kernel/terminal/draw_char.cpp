	void terminal::draw_char(u8 character, u32 color)
	{
		if (character == '\n')
		{
			cursor.row++;
			cursor.column = 0;
			return;
		}

		auto screen = screen_manager::instance;

		auto glyph = font[character];

		for (u8 y = 0; y < 15; y++)
		{
			for (u8 x = 0; x < 9; x++)
			{
				if (glyph[y] & (1 << x))
				{
					screen->draw_pixel(cursor.column * 9 + (9 - x - 1), cursor.row * 16 + y, color);
				}
			}
		}

		cursor.column++;
		if (cursor.column >= columns)
		{
			cursor.row++;
			cursor.column = 0;
		}
	}
